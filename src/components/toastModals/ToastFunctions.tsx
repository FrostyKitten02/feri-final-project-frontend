import { toast, Bounce } from "react-toastify"

export const toastError = (error: any) => {
    toast.error(`An error has occured: ${error.message}`, {
      position: "top-center",
      autoClose: 5000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      progress: undefined,
      theme: "light",
      transition: Bounce,
    });
}

